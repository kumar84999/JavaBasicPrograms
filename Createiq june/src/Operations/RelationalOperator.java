package Operations;

public class RelationalOperator {
	public static void main(String []args) {
		//comparision <,>,<=,>=;
		int a=10;
		int b=20;
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		//Equality ==,!=;
		System.out.println(a==b);
		System.out.println(a!=b);
	}

}
