package com.Basics;

public class DatatypesDemo {
	public static void main(String[] args) {

		System.out.println("<----------- primitives --------------->");
		boolean isEligible = false;
		System.out.println("boolean value: " + isEligible);
		char gender = 'F';
		System.out.println(gender);
		float quanitityInStock = 150.14f;
		System.out.println(quanitityInStock);
		double amount = 10000.00;
		System.out.println(amount);
		byte humanAge = 90;
		System.out.println(humanAge);
		short value = 32000;
		System.out.println(value);
		int number = 123456789;
		System.out.println(number);
		long longNumers = 12345678945L;
		System.out.println(longNumers);

		System.out.println("<----------- Non primitive --------------->");
		String name = "createiq tech";
		System.out.println(name);

	}

}


