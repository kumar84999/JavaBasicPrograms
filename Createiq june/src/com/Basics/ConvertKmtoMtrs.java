package com.Basics;

public class ConvertKmtoMtrs {
	
		public static void main(String[] args) {
			// centimeters
			float cm = 200000.0f;
			// meters
			float meters = cm / 100.00f;
			System.out.println(meters + " meters");

			// kilometers
			float kilometer = cm / 100000.00f;
			System.out.println(kilometer + " km");
		}

	}


